    <h2>
      <a href="/r/culinaria-capixaba">Culinária Capixaba</a> — Torta Capixaba
    </h2>
    <h2>Ingredientes</h2>
    <ul>
      <li>200g de bacalhau dessalgado, cozido e desfiado;</li>
      <li>200g de camarões médios frescos e limpos;</li>
      <li>200g de carne de siri desfiada;</li>
      <li>200g de sururu;</li>
      <li>200g de peixe branco cortado em cubos;</li>
      <li>
        600g de palmito pupunha fresco picado em cubos (aferventado em água com
        pouco sal ou cozido no vapor);
      </li>
      <li>100ml de azeite de oliva extra virgem;</li>
      <li>100g de cebola branca cortada em cubos pequenos;</li>
      <li>50g de alho picado;</li>
      <li>100g de tomates cortados em cubos pequenos sem sementes;</li>
      <li>1 maço de coentro picado;</li>
      <li>20ml de óleo de urucum ou uma colher de sobremesa de colorau;</li>
      <li>100g de azeitonas verdes inteiras;</li>
      <li>6 ovos inteiros;</li>
      <li>10ml de suco de limão;</li>
      <li>Sal à gosto;</li>
      <li>Pimenta do reino moída na hora ou pimenta de cheiro à gosto;</li>
      <li>100g de cebola branca cortada em rodelas não muito fininhas.</li>
    </ul>
    <div class="spacer">&nbsp;</div>
    <h2>Modo de preparo</h2>
    <p>
      Em uma panela, aqueça metade do azeite reservando o restante para untar a
      panela de barro, na qual vai se colocar a torta para assar.
    </p>
    <p>
      Quando o azeite estiver quente, junte a cebola picada em cubos pequenos
      até que fique transparente e em seguida adicione o alho. Refogue um pouco
      mais.
    </p>
    <p>
      Acrescente o tomate picado e o urucum (colorau). Mexa e deixe o tomate
      murchar.
    </p>
    <p>
      Em seguida adicione o sururu, o siri e o bacalhau e mexa. Coloque o peixe
      e o camarão. Mexa e aguarde por 5 minutos para o camarão ficar rosado.
    </p>
    <p>
      Junte o suco do limão, misture e introduza o palmito e o coentro. Deixe
      esfriar.
    </p>
    <p>
      Quando estiver frio, pegue os ovos, separe as claras das gemas. Reserve as
      claras e bata as gemas. Em seguida, misture as gemas batidas à massa dos
      frutos do mar com o palmito para dar liga.
    </p>
    <p>
      Unte a panela de barro com o azeite restante. Pré aqueça o forno em 180ºC.
    </p>
    <p>
      Bata as claras em neve. Coloque a mistura na panela de barro e cubra-a com
      as claras em neve. Distribua por cima as cebolas em rodelas com uma
      azeitona em cada rodela de cebola.
    </p>
    <p>
      Leve a torta ao forno pré-aquecido a 180° C por aproximadamente 30 minutos
      ou até que as claras fiquem secas e douradas.
    </p>
    <p>Retire do forno, coloque a panela no suporte de ferro e sirva.</p>
