      <h2>
        <a href="/r/culinaria-capixaba">Culinária Capixaba</a> — Moqueca Capixaba
      </h2>
      <h2>Ingredientes</h2>
      <ul>
        <li>1kg postas de pintado</li>
        <li>5 colheres de óleo</li>
        <li>1/2 kg de camarão</li>
        <li>5 dentes de alho</li>
        <li>2 cebolas pequena (em cubos)</li>
        <li>1 maço de coentro</li>
        <li>1 pimentão pequeno</li>
        <li>1 tomate</li>
        <li>100 ml leite de coco</li>
        <li>200 ml extrato de tomate</li>
        <li>1 colher de chá de urucum</li>
        <li>Sal a gosto</li>
      </ul>
      <div class="spacer">&nbsp;</div>
      <h2>Modo de preparo</h2>
      <p>
        Começamos fazendo o fundo da panela, de preferência de barro, logo em
        seguida acrescente óleo, as cebolas em cubos, o pimentão e o tomate.
        Deixe no fogo até aquecer.
      </p>
      <p>
        Em uma outra panela, prepare o <strong>molho</strong>. Comece refogando
        o alho e a cebola picada no óleo, em seguida misture a massa de tomate e
        o coentro, mexa até ferver.
      </p>
      <p>
        Com a panela na moqueca quente, é a hora de arrumar as postas de
        pintado, uma ao lado da outra. Depois do molho ter fervido, junte ele ao
        peixe na panela de barro. Para concluir, coloque os camarões e o
        restante dos ingredientes.
      </p>
      <p>
        O sal depende do gosto de cada pessoa, pode ser posto somente no peixe
        ou então na preparação do molho.
      </p>
